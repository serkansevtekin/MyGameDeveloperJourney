    Template Method → Oyun döngülerinde tekrar eden yapıların soyutlanması.

    Decorator → Power-up sistemi veya efekt ekleme.

    Adapter / Facade → Dış sistem (Firebase, Ads, Audio) entegrasyonu.

Nadiren gereken (teorik veya büyük ölçekli):

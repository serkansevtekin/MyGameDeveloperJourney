    - Strategy – Farklı saldırı/hareket davranışları.

    - Command – Input veya undo sistemleri.

    - Dependency Injection / Service Locator – Modülerlik ve test kolaylığı.

    - Prototype – Prefab kopyalama, klonlama sistemleri.
    
    - Builder – Karmaşık nesne veya level kurulumlarında.

İkinci sırada (daha az ama bazen faydalı):

    Mediator → UI panel iletişimi, modül bağımsızlığı.


// Refactoring

/*
Refactoring:
        - Kodun işlevini değiştiröeden iyileştirilmesi işlemidir
        - Amaç: okunabilirlik, sürdürebilirlik ve test edilebilirlik arttırmak
        - Örnekler:
                - Uzun methodları parçalara ayırmak
                - Tekrarlanan kodları fonksiyon/motoda dönüştürmek
                - Anlamlı değişken/sınıf isimlendirmesi yapmak
                - Karmaşık koşulları sadeleştirmek
        
        Özet:
                - Code Smell -> problem işareti
                - Refactoring -> problemi çözme yöntemi

                Örnek akış:
                        -  "Kodda uzun method tespit ettin -> Code Smell"
                        -  "Methodu çok parçalara böldün -> Refactoring"

                Bu, temiz ve sürdürülebilir kod yazmanın temel mekanizmasıdır
*/
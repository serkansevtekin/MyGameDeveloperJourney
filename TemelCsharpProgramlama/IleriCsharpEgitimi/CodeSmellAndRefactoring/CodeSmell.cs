    // Code Smell

/*
    Code Smell:
            - Kodun tasrım veya kalite sorunlarını işaret eden belirtileridir.
            - Genellikle derleyici hatası üretmez, ama bakım, okunabilirlik ve genişletilebilirliği zorlaştırır.
            - Örnekler:
                    - Uzun methodlar (Long Method)
                    - Tek bir sınıfa çok fazla sorumluluk (God Class)
                    - Tekrarlanan kod (Duplicate Code)
                    - Anlamsız isimlendirmeler (Poor Naming)

            - Amaç: "Kötü koku" ile kodun problemli olduğun fark etmek
            
            Özet:
                - Code Smell -> problem işareti
                - Refactoring -> problemi çözme yöntemi

                Örnek akış:
                        -  "Kodda uzun method tespit ettin -> Code Smell"
                        -  "Methodu çok parçalara böldün -> Refactoring"

                Bu, temiz ve sürdürülebilir kod yazmanın temel mekanizmasıdır
*/